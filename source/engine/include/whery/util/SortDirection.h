#ifndef H_WHERY_SORTDIRECTION
#define H_WHERY_SORTDIRECTION

namespace whery {

/**
\brief The values of this enum represent possible directions in which elements should be sorted.
*/
enum SortDirection
{
	/** The elements should be sorted in ascending order. */
	ASC,

	/** The elements should be sorted in descending order. */
	DESC
};

}

#endif
